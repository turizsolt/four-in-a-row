package main.java;

public class DropData {
    private int x;
    private int y;
    private int color;

    public DropData(int _x, int _y, int _color) {
        x = _x;
        y = _y;
        color = _color;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getColor() {
        return color;
    }
}
